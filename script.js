let menuBar = document.getElementById("menu-bar");

let isMenuOpened = false;
function toggleMenuBar() {
  isMenuOpened = !isMenuOpened;

  let sideBar = document.getElementById("sidebar");

  if (isMenuOpened) {
    sideBar.style.display = "flex";
  }
  if (!isMenuOpened) {
    sideBar.style.display = "none";
  }
}

window.addEventListener("load", handelOnWindowLoad);

function createProduct(src, name, price) {
  return `<div class="product">
  <div class="product_image">
    <img src="${src}" />
  </div>
  <div class="product_name">${name}</div>
  <div class="product_price">RS.${price}</div>
  <button class="add_to_cart">Add to cart</button>
  </div>`;
}

function handelOnWindowLoad() {
  const products = document.getElementById("products");
  productsHtmlString = prods.reduce(function (acc, curVal) {
    const prdHMLStr = createProduct(curVal.img, curVal.name, curVal.price);
    return acc + prdHMLStr;
  }, "");
  products.innerHTML = productsHtmlString;
}
